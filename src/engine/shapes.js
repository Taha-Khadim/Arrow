// Direction encoding: 0=empty, 1=up, 2=right, 3=down, 4=left
// Directions: 1=up(-row), 2=right(+col), 3=down(+row), 4=left(-col)

const DIR_DR = { 1: -1, 2: 0, 3: 1, 4: 0 };
const DIR_DC = { 1: 0, 2: 1, 3: 0, 4: -1 };
const OPPOSITE = { 1: 3, 2: 4, 3: 1, 4: 2 };

// Build hard puzzles: peel one arrow at a time (strict ordering).
// Only the ONE arrow you peel gets a clear-exit direction.
// All other arrows in that layer get a BLOCKING direction (pointing inward
// toward other arrows), so you MUST solve in exact sequence.
export function assignDirections(mask, seed = 42, difficulty = 0.5) {
  let s = seed;
  const next = () => { s = (s * 1103515245 + 12345) & 0x7fffffff; return s; };

  const rows = mask.length;
  const cols = mask[0].length;
  const grid = mask.map(row => row.map(v => (v ? -1 : 0)));
  const cells = [];

  for (let r = 0; r < rows; r++)
    for (let c = 0; c < cols; c++)
      if (mask[r][c]) cells.push({ r, c });

  const remaining = new Set(cells.map(({ r, c }) => `${r},${c}`));
  const solveOrder = [];

  // Peel ONE cell at a time — the cell that gets a clear exit.
  // All others that COULD exit get a blocking direction instead.
  while (remaining.size > 0) {
    const candidates = [];

    for (const key of remaining) {
      const [r, c] = key.split(',').map(Number);
      const clearDirs = [];
      const blockedDirs = [];

      for (let d = 1; d <= 4; d++) {
        const dr = DIR_DR[d], dc = DIR_DC[d];
        let cr = r + dr, cc = c + dc;
        let blocked = false;
        while (cr >= 0 && cr < rows && cc >= 0 && cc < cols) {
          if (remaining.has(`${cr},${cc}`)) { blocked = true; break; }
          cr += dr;
          cc += dc;
        }
        if (!blocked) clearDirs.push(d);
        else blockedDirs.push(d);
      }

      if (clearDirs.length > 0) {
        candidates.push({ r, c, key, clearDirs, blockedDirs });
      }
    }

    if (candidates.length === 0) {
      // Shouldn't happen, but fallback
      for (const key of remaining) {
        const [r, c] = key.split(',').map(Number);
        grid[r][c] = (next() % 4) + 1;
      }
      remaining.clear();
      break;
    }

    // Shuffle candidates
    for (let i = candidates.length - 1; i > 0; i--) {
      const j = next() % (i + 1);
      [candidates[i], candidates[j]] = [candidates[j], candidates[i]];
    }

    // Pick ONE to actually exit (give it a clear direction)
    const winner = candidates[0];
    const exitDir = winner.clearDirs[next() % winner.clearDirs.length];
    grid[winner.r][winner.c] = exitDir;
    remaining.delete(winner.key);
    solveOrder.push(winner.key);

    // All OTHER candidates that could exit: give them a BLOCKED direction
    // (pointing toward another arrow) so they can't exit yet.
    for (let i = 1; i < candidates.length; i++) {
      const c = candidates[i];
      if (!remaining.has(c.key)) continue;

      if (c.blockedDirs.length > 0) {
        // Point toward another arrow = blocked
        grid[c.r][c.c] = c.blockedDirs[next() % c.blockedDirs.length];
      } else {
        // No blocked direction available — leave for next iteration
        // (it will become the next winner or get a blocked dir later)
      }
    }

    // Also assign blocked directions to non-candidate remaining cells
    // that haven't been assigned yet (inner cells with no clear path)
    for (const key of remaining) {
      const [r, c] = key.split(',').map(Number);
      if (grid[r][c] !== -1) continue; // already assigned

      const blockedDirs = [];
      for (let d = 1; d <= 4; d++) {
        const dr = DIR_DR[d], dc = DIR_DC[d];
        let cr = r + dr, cc = c + dc;
        while (cr >= 0 && cr < rows && cc >= 0 && cc < cols) {
          if (remaining.has(`${cr},${cc}`)) { blockedDirs.push(d); break; }
          cr += dr;
          cc += dc;
        }
      }
      if (blockedDirs.length > 0) {
        grid[r][c] = blockedDirs[next() % blockedDirs.length];
      }
    }
  }

  // Final pass: any unassigned cells get a random direction
  for (let r = 0; r < rows; r++)
    for (let c = 0; c < cols; c++)
      if (grid[r][c] === -1) grid[r][c] = (next() % 4) + 1;

  return grid;
}

// Procedural mask generator for levels beyond 100
export function generateProceduralMask(levelNum, targetCount, seed) {
  let s = seed;
  const next = () => { s = (s * 1103515245 + 12345) & 0x7fffffff; return s; };

  // Grid size scales with target count
  const side = Math.max(8, Math.ceil(Math.sqrt(targetCount * 1.6)));
  const rows = side;
  const cols = side;
  const mask = Array.from({ length: rows }, () => Array(cols).fill(0));

  // Seed a random connected shape by random walk from center
  const cx = Math.floor(rows / 2);
  const cy = Math.floor(cols / 2);
  const filled = new Set();
  let r = cx, c = cy;
  filled.add(`${r},${c}`);
  mask[r][c] = 1;

  const dirs = [[-1, 0], [1, 0], [0, -1], [0, 1]];
  while (filled.size < targetCount) {
    const [dr, dc] = dirs[next() % 4];
    const nr = Math.max(0, Math.min(rows - 1, r + dr));
    const nc = Math.max(0, Math.min(cols - 1, c + dc));
    if (!filled.has(`${nr},${nc}`)) {
      filled.add(`${nr},${nc}`);
      mask[nr][nc] = 1;
    }
    r = nr;
    c = nc;
  }

  return mask;
}

